<p>At night, Andrian Kurniady and I made a bet that if we solved problem <a href="http://acm.uva.es/p/v3/302.html">302 - John's Trip</a>, then we will be the champion tomorrow. This problem is really bugging me for the last 2 weeks, I cannot get it accepted. When we discuss the problem again, it appeared that I misread the problem statement :P, the starting node is the same with the ending node. If so, then this problem can be solved easily just like problem <a href="http://acm.uva.es/p/v100/10054.html">10054 - Necklace</a>, it's just an <a href="http://en.wikipedia.org/wiki/Eulerian_path">Eulerian Path</a>. And yes, we did get problem 302 accepted! (this is a sign that we'll do well tomorrow).</p>

<p>Another good sign is the seat number. When the seat number was randomized (during the practice session), YoiAC got seat number 32. Andoko Chandra said it means that we are the biggest special numbered team around here. He believes that power of 2 are special numbers (1,2,4,8,..,32,64,..) and since no other team got number bigger than us (no seat number 64) therefore we are the most special :)</p>

