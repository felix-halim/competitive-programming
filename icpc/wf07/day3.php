<p>A little description about the Japanese Cultural Experience:
<ul>
<li>Shodo (Japanese Calligraphy), this showed us how to write the characters. One can tell the spirit/emotion of the writer just by looking at the writing. At the end, we were allowed to write our own and take home the writing as souvenirs.
<li>Zazen (Meditation), this is where you enter the "State of Nothingness". It's to empty your mind, hoping that new ideas can come up.
<li>Anime (Graphic arts), I only took a quick look for this so I can't tell much about it.
<li>Aikido (Self Defense Martial Arts), two kinds of moves were demonstrated and practiced in this experience. One is when you were caught from the back, and second is when your arm was caught. The moves show how to escape after get caught.
<li>Origami (Paper folding), this tells you how to fold paper into some kind of form (birds, mountains, person, tea cup, etc... see the picture above).
</ul>
</p>

<p>I tried the Aikido, Zazen, Anime (at a glance), and Shodo. So I learned a bit how to escape, how to enter the "State of Nothingness" and how to write characters.</p>

