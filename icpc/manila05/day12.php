<h1>Day 2, Take off to Manila (27 Oct 2005)</h1>
<p>Still at the KLIA, we ate breakfast. Some of us drink Starbucks Coffee, the other ate Noodles (some says the sauce is made of medicine?). At 11 AM, we then take off to Manila and went to Richmonde Hotel (the same hotel as last year). The transportation used was a taxi. It's amazing to imagine how 11 + 1 person along with their bags fit in. We met Hong Kong university (the champion) and the others during the hotel registration.</p>

<p>In the afternoon we went to the Mega-mall at 9 PM for dinner. The shops over there were almost closed at 10 PM.</p>

